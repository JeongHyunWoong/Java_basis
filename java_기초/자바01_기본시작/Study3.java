package 자바01_기본시작;

public class Study3 {

	public static void main(String[] args) {

		//변수의 선언		
		int number;		
		//값의 대입
		number = 100;		
		number = number + 100;		
		number = number - 10;		
		System.out.println("number: " + number);
		
		//변수의 선언		
		String name;
		name = "헬로우";
		name = name + " 자바";
		System.out.println("name: " + name);

	}

}
