package 자바04_제어문시작;

import java.util.Scanner;

public class while22 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int i=0;
		while(i<5)
		{
			System.out.println("while문 장마가 시작되었어요!");
			i++;
		}
		for(int k=0; k<5; k++)
		{
			System.out.println("for문 장마가 시작되었어요!");
		}
	}	
}			
			
			
		
